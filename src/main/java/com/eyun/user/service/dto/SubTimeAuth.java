package com.eyun.user.service.dto;

public class SubTimeAuth {
	
	private String frist;
	private String last;
	private int page;
	private int size;
	public String getFrist() {
		return frist;
	}
	public void setFrist(String frist) {
		this.frist = frist;
	}
	public String getLast() {
		return last;
	}
	public void setLast(String last) {
		this.last = last;
	}
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
	
	

}
